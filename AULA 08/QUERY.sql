-- LEFT JOIN
SELECT EX.NOME, FX.NOME
FROM TABELAEX AS EX
LEFT JOIN TABELAFX AS FX
ON EX.NOME = FX.NOME;

-- RIGHT JOIN
SELECT EX.NOME, FX.NOME
FROM TABELAEX AS EX
RIGHT JOIN TABELAFX AS FX
ON EX.NOME = FX.NOME;

-- LEFT JOIN COM NULL
SELECT EX.NOME, FX.NOME
FROM TABELAEX AS EX
LEFT JOIN TABELAFX AS FX
ON EX.NOME = FX.NOME
WHERE FX.NOME IS NULL;